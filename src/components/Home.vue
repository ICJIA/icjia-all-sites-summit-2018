            <div class="mr-auto">
              <p>Criminal Justice Coordinating Councils (CJCCs) are partnerships of decision makers who have a stake in the
                effective administration of justice. Typically the partnerships are organized at the county level and include
                representatives from:</p>
              <ul>
                <li>The 3 branches of government (executive, judicial, and legislative)</li>
                <li>Multiple levels of government (city, county, and state agencies)</li>
                <li>Allied stakeholders from various other governmental entities, such as education or health and human services,
                  community based organizations, service providers, and citizens</li>
              </ul>
              <p>
                CJCCs follow a structured, data-driven planning process to identify, analyze, solve, and manage justice system issues. This
                work leads to better understanding of crime and criminal justice problems, greater cooperation among agencies
                and units of local government, clearer objectives and priorities, more effective resource allocation, and
                better quality criminal justice programs and personnel. Taken together, these results can increase public
                confidence in and support for criminal justice processes, enhancing system performance and, ultimately, the
                integrity of the law.</p>



